#       Inicio, Limite, Aumento
for i in range(0, 5, 2):
    print(i)
print("---------------------")

#         Inicio, Limite
for i in range(3, 5):
    print(i)
print("---------------------")

#            Limite  (Por defecto, inicio en el 0 y aumenta de 1 en 1)
for i in range(5):
    print(i)
print("---------------------")

for letra in "Hola Mundo":
    print(letra)
print("---------------------")

listaFrutas = ["Banana", "Cereza", "Manzana"]
for fruta in listaFrutas:
    print(fruta)
print("---------------------")

tuplaAutos = ("Audi", "Mazda", "Honda")
for auto in tuplaAutos:
    print(auto)
print("---------------------")