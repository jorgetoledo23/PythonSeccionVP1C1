"str -> String"
print(type("Cadena de Texto"))

"int -> Integer (Entero)"
print(type(10))

"float -> Numero de Punto Flotante"
print(type(10.5))

"bool -> Boleanos"
print(type(True))
print(type(False))

"list -> Listado de Elementos"
frutas = ["Manzana", 1, True]
print(type(frutas))

"tuple -> Tupla"
frutas2 = ("Manzana", 1, True)
print(type(frutas2))

"dict -> Diccionario de Datos"
frutas3 = {1 : "Manzana",
2 : "Pera",
3 : "Cereza"}
print(type(frutas3))